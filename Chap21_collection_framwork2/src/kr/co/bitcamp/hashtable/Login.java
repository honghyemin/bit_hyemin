package kr.co.bitcamp.hashtable;

public class Login {
    
    private String ID;
    private String PW;
    
    public Login(String iD, String pW) {
        super();
        ID = iD;
        PW = pW;
    }

    public String getID() {
        return ID;
    }

    public String getPW() {
        return PW;
    }
    
    
    
    

}
